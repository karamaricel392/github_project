def add_numbers(a: float, b: float) -> float:
    """
    Add two numbers and return the result.
    
    Args:
        a (float): The first number to be added.
        b (float): The second number to be added.
        
    Returns:
        float: The sum of `a` and `b`.
    """
    return a + b

# Example usage
result = add_numbers(5.5, 3.2)
print(result)
